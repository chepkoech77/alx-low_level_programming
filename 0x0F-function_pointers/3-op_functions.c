#include "3-calc.h"

/**
 * op_add - adds
 * @a: param
 * @b: param
 *
 * Return: Integer
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub
 * @a: param
 * @b: param
 *
 * Return: Integer
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - muls
 * @a: param
 * @b: param
 *
 * Return: Integer
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides
 * @a: param
 * @b: param
 *
 * Return: Integer
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus
 * @a: param
 * @b: param
 *
 * Return: Integer
 */


int op_mod(int a, int b)
{
	return (a % b);
}

